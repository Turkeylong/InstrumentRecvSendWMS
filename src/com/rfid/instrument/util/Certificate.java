package com.rfid.instrument.util;

public class Certificate {
	String certificateNumber;
	String deviceName;
	String deviceState;
	String certificateCreateDate;
	String certificateFilingDate;
	String certificateReleaseDate;
	String certificateReleaseInfo;
	String certificateState;
	public String getCertificateNumber() {
		return certificateNumber;
	}
	public void setCertificateNumber(String certificateNumber) {
		this.certificateNumber = certificateNumber;
	}
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	public String getDeviceState() {
		return deviceState;
	}
	public void setDeviceState(String deviceState) {
		this.deviceState = deviceState;
	}
	public String getCertificateCreateDate() {
		return certificateCreateDate;
	}
	public void setCertificateCreateDate(String certificateCreateDate) {
		this.certificateCreateDate = certificateCreateDate;
	}
	public String getCertificateFilingDate() {
		return certificateFilingDate;
	}
	public void setCertificateFilingDate(String certificateFilingDate) {
		this.certificateFilingDate = certificateFilingDate;
	}
	public String getCertificateReleaseDate() {
		return certificateReleaseDate;
	}
	public void setCertificateReleaseDate(String certificateReleaseDate) {
		this.certificateReleaseDate = certificateReleaseDate;
	}
	public String getCertificateReleaseInfo() {
		return certificateReleaseInfo;
	}
	public void setCertificateReleaseInfo(String certificateReleaseInfo) {
		this.certificateReleaseInfo = certificateReleaseInfo;
	}
	public String getCertificateState() {
		return certificateState;
	}
	public void setCertificateState(String certificateState) {
		this.certificateState = certificateState;
	}
	@Override
	public String toString() {
		return "Certificate [certificateNumber=" + certificateNumber
				+ ", deviceName=" + deviceName + ", deviceState=" + deviceState
				+ ", certificateCreateDate=" + certificateCreateDate
				+ ", certificateFilingDate=" + certificateFilingDate
				+ ", certificateReleaseDate=" + certificateReleaseDate
				+ ", certificateReleaseInfo=" + certificateReleaseInfo
				+ ", certificateState=" + certificateState + "]";
	}
}
